#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*str;

	str = (char *)s;
	i = 0;
	if (n == 0)
		n = 0;
	else
	{
		while (n > 0)
		{
			str[i] = '\0';
			n--;
			i++;
		}
	}
}
