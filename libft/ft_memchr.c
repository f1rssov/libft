#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*str;
	size_t	i;

	i = 0;
	str = (char *)s;
	while (i != n)
	{
		if ((unsigned char)*str == (unsigned char)c)
			return (str);
		str++;
		i++;
	}
	return (NULL);
}
